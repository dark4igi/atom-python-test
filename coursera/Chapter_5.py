###Loops
## infinite loops - 'while'
#example
n = 5
while n > 0:
    print (n)
    n = n - 1
print ('Done, n = ', n)

## break - to break loop

## continue - to break itteration on the loop

## Define loops - 'for'
#example
for i in [5,4,3,2,1]:
    print (i)
print ('Done')

#
for i in range (3, 16, 2): # in range (start, finish, step)
    print (i)
print ('Done')
